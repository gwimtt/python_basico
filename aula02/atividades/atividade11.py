"""
11.	Faça um Script em Python que solicite a largura e a altura de um retângulo. 
Em seguida, imprima para o usuário quantos metros quadrados possui a área total do quadrado. 
"""

largura_ret = float(input('Digite a largura do seu retângulo: '))
altura_ret = float(input('Digite a altura do seu retângulo: '))

print(f'A área total do retângulo é igual a {largura_ret*altura_ret} metros quadrados.')