# operadores aritméticos (continuação)

# valores para testes no aprendizado
valor1 = 8
valor2 = 11
print("Valor 1: 8")
print("Valor 2: 11")
print("-"*50)

# exponenciação
print("Exponenciação:", valor1**3)
print("-"*50)

# divisão padrão e sem casas decimais
print("Divisão padrão:", valor1/valor2)
print("Divisão sem decimais", valor1//valor2)
print("-"*50)

# módulo (resto) da divisão - mostra o resto da divisão
print("Módulo (resto) da divisão:",valor2%3)
print("-"*50)

# operadores de comaparação (relacionais)
print("Valor 1 é maior do que valor 2?:",valor1>valor2)
print("Valor 1 é maior ou igual o valor 2?:",valor1>=valor2)
print("Valor 1 é menor do que o valor 2?:",valor1<valor2)
print("Valor 1 é menor ou igual o valor 2?:",valor1<=valor2)
print("Valor 1 é igual ao valor 2?:",valor1==valor2)
print("Valor 1 é diferente de valor 2?:",valor1!=valor2)